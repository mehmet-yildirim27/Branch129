package day12loops;

public class C02_ForLoopSayiKullanmadanJava {

    public static void main(String[] args) {

        /*
        1'den 100'e kadar hic sayi kullanmadan console yazdiriniz
         */

        // System.out.println((char)101);   (Herhangi bir sayinin Ascii karsiligini bulmak icin kullaniriz)
        for (int i = 'e'/'e';   i <'e' ; i++) {//e 'nin Ascci degeri 101 'd'=100
            System.out.print(i+" ");
        }
    }
}
